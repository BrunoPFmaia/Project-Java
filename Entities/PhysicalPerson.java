package Entities;

public class PhysicalPerson {
    private int cpf;
    private int idade;

    public PhysicalPerson() {
        int cpf;
        int idade;
    }

    public int getCpf() {
        return cpf;
    }

    public void setCpf(int cpf) {
        this.cpf = cpf;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }
}

